#Codigo para verificar a idade das pessoas
Idade = int(input("Entre com a sua idade: "))

if Idade < 60 and Idade >= 18:
    print("Voce é Maior de idade!")
elif Idade < 18:
    print("Voce é Menor de idade!")
elif Idade >= 60:
    print("Voce é da Terceira idade!")

