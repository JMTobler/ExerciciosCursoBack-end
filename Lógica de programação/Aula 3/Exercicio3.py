#Codigo para verificar se o numero é positivo/negativo ou igual a Zero
Num = int(input("Entre com o seu numero:"))

if Num > 0:
  print("Seu Numero é positivo")
if Num < 0:
  print("Seu Numero é negativo")
if Num == 0:
  print("Seu Numero é zero")


